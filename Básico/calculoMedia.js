// Escreva uma função que receba três números como parâmetros e retorne a média deles.

function media(a, b, c) { // Função que recebe três números como parâmetros
    let media = (a + b + c) / 3; // Cálculo da média
    return media; // A função deve retornar o resultado do cálculo da média
}

console.log(media(7, 8, 6)); // Imprimindo o resultado da média entre 7, 8 e 6 no console